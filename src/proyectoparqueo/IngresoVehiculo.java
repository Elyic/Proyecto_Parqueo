    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtMatricula.getText().isEmpty() || txtColor.getText().isEmpty() | txtMarca.getText().isEmpty() || txtModelo.getText().isEmpty()) {
          JOptionPane.showMessageDialog(null, "Algun campo se encuentra vacio");
        }else{
            Vehiculo vehiculo = new Vehiculo();
            if (vehiculo.InsertarTablaVehiculo(txtMatricula.getText(), txtModelo.getText(), txtColor.getText(), txtMarca.getText())) {
                JOptionPane.showMessageDialog(null, "Vehiculo ingresado correctamente!");
                btnGuardar.setEnabled(false);
            }else{
                JOptionPane.showMessageDialog(null, "Ocurrio un error al ingresar el vehiculo!");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

