    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if (txtMatricula.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo matricula se encuentra vacio!");
        }else{
            Vehiculo vehiculo = new Vehiculo();
            if (vehiculo.ConsultaTablaVehiculo(txtMatricula.getText())) {
                JOptionPane.showMessageDialog(null, "El vehiculo ya se encuentra en la base de datos!");
            }else{
                txtColor.setEditable(true);
                txtMarca.setEditable(true);
                txtModelo.setEditable(true);
                txtMatricula.setEditable(false);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

