    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      if(txtMatricula.getText().isEmpty() || txtMatricula.getText().equals(" "))
      {
          JOptionPane.showMessageDialog(null, "El campo matricula esta vacio!");
      }else
      {
          txtMatricula.setEditable(false);
        if(vehiculo.ConsultaTablaVehiculo(txtMatricula.getText()))
        {
        txtModelo.setText(vehiculo.sModeloGlobal);
        txtColor.setText(vehiculo.sColorGlobal);
        txtMarca.setText(vehiculo.sMarcaGlobal);
        }
        else 
            {                
                txtModelo.setEditable(true);
                txtColor.setEditable(true);
                txtMarca.setEditable(true);    
            }
      }
        
    }//GEN-LAST:event_jButton2ActionPerformed

