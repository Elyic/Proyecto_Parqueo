    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(cliente.InsertarTablaCliente(txtNombre.getText(), txtDPI.getText(), txtApellido.getText()) && vehiculo.InsertarTablaVehiculo(txtMatricula.getText(), txtModelo.getText(), txtColor.getText(), txtMarca.getText()))
       {
           cliente.ConsultaTablaCliente(txtDPI.getText());
           asociarcv.InsertarTablaAsociarCV(cliente.IDGlobal, txtMatricula.getText());
           JOptionPane.showMessageDialog(null, "Ingresado");
       }
       else
            {
            JOptionPane.showMessageDialog(null, "Error al Ingresar");
            }
    }//GEN-LAST:event_jButton1ActionPerformed

