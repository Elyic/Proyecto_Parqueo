    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JasperReport jr = null;
        Conexion conexion = new Conexion();
        Connection connecta = conexion.ConectarABD();
        String url = System.getProperty("user.dir");
        try {
            Map parametro = new HashMap();
            parametro.put("parameter1", jComboBox1.getSelectedItem());
            parametro.put("parameter2", jComboBox2.getSelectedItem());
            parametro.put("logo",this.getClass().getResourceAsStream("/Reportes/Parqueo.jpeg"));
            jr = (JasperReport) JRLoader.loadObjectFromFile(url+"\\src\\Reportes\\report1_1.jasper");
            JasperPrint jp = JasperFillManager.fillReport(jr, parametro, connecta);
            JasperViewer jv = new JasperViewer(jp,false);
            jv.setVisible(true);
            jv.setTitle("Reporte de Facturas entre fechas");
        } catch (JRException ex) {
            Logger.getLogger(ReporteFechas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

